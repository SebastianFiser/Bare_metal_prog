static framebuffer_t fb;

typedef struct multiboot2_info {
    uint32_t total_size;
    uint32_t reserved;
} multiboot2_info_t;

typedef struct multiboot2_tag {
    uint32_t type;
    uint32_t size;
} multiboot2_tag_t;

typedef struct multiboot2_tag_framebuffer {
    uint32_t type;
    uint32_t size;
    uint64_t framebuffer_addr;
    uint32_t framebuffer_pitch;
    uint32_t framebuffer_width;
    uint32_t framebuffer_height;
    uint8_t framebuffer_bpp;
    uint8_t framebuffer_type;
    uint16_t reserved;
} __attribute__((packed)) multiboot2_tag_framebuffer_t;

enum {
    MULTIBOOT2_TAG_END = 0,
    MULTIBOOT2_TAG_FRAMEBUFFER = 8
};

bool fb_is_available(void) {
    return (fb.address != NULL && fb.width > 0 && fb.height > 0 && fb.bpp == 32);
}

uint32_t fb_get_width(void) {
    return fb.width;
}

uint32_t fb_get_height(void) {
    return fb.height;
}

uint32_t fb_get_pitch(void) {
    return fb.pitch;
}

uint32_t fb_get_address(void) {
    return (uint32_t)fb.address;
}

